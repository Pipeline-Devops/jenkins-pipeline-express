// add
function add(a, b) {
  return a + b;
}

// divide
function divide(a, b) {
  return a / b;
}

// multiple
function multiply(a, b) {
  return a * b;
}

// subtract
function subtract(a, b) {
  return a - b;
}

module.exports = {
  add: add,
  divide: divide,
  multiply: multiply,
  subtract: subtract
};
